package Ex_9_SwitchCondition;

public class Lab089_Switch6 {
    public static void main(String[] args){
        char code = 'C';
        switch (code){
            default:
                System.out.println("hiiiiiiiiii");
                break;
            case 'A':
                System.out.println("88");
                break;
            case 'B':
                System.out.println("77");
                break;
        }
    }
}
