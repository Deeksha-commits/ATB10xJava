package Ex_7_Increment_Decrement;

public class Lab068_ID7 {
    public static void main(String[] args){
        int a = 10;
        System.out.println(++a + ++a);
        //++ means value of ++a is 11 and a value is also 11
        //   so 11+
        //++a means value of ++a is 12 and a value is also 12
        // so 11+12=23
        System.out.println(a);

    }
}
