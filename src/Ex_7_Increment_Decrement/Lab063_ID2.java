package Ex_7_Increment_Decrement;

public class Lab063_ID2 {
    public static void main(String[] args){
        int a = 10;
        System.out.println(++a);
        System.out.println(a);

        //post increment means print first means assign the value first and then increment
        int a_post = 10;
        System.out.println(a_post++);
        System.out.println(a_post);
    }
}
