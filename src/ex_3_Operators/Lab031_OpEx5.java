package ex_3_Operators;

public class Lab031_OpEx5 {
//unary operator
    public static void main(String[]args){

        int a = 10;
        int b = -10;
        boolean c = true;
        boolean d = false;
        System.out.println(~a);
        System.out.println(~b);
        System.out.println(!c);
        System.out.println(!d);

    }
}
