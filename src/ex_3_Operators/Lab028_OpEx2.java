package ex_3_Operators;

public class Lab028_OpEx2 {
    //arithmetic operators
    public static void main(String[] args) {


        int a = 20;
        int b = 16;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        int c = 20;
        float d = 10.5f;
        System.out.println(c+d);
        System.out.println(c-d);
        System.out.println(c*d);
        System.out.println(c/d);
        System.out.println(c%d);

    }
}
