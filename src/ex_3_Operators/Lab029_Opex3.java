package ex_3_Operators;

public class Lab029_Opex3 {
    //unary operators
    public static void main(String[] args){

        int a = 15;
        System.out.println(a++);
        System.out.println(a);

        System.out.println(++a);
        System.out.println(a);

        System.out.println(a--);
        System.out.println(a);

        System.out.println(--a);
        System.out.println(a);
    }
}
